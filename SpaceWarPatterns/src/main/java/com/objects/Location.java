package com.objects;

import java.util.*;

/**
 * 
 */
public class Location {

	/**
	 * Default constructor
	 */
	public Location(int x, int y) {
		X = x;
		Y = y;
	}

	/**
	 * 
	 */
	public int X;

	/**
	 * 
	 */
	public int Y;


	/**
	 * @param x 
	 * @param y 
	 * @return
	 */
	public void setLocation(int x, int y) {
		X = x;
		Y = y;
	}

}